package ru.otus.hw03TestFramework;

public class SimpleClass {

    public int getSum(int a, int b){
        return a + b;
    }

    public double getDiv(int a, int b){
        return (double) a / b;
    }

    public int getSub(int a, int b){
        return  a - b;
    }

    public long getMult(int a, int b){
        return a * b;
    }
}
